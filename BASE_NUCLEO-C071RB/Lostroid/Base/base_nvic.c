/* Base NVIC initialize
-------------------------------------------------------------------*/
void f_base_nvic_init(void)
{
    f_base_nvic_flag_set(m_NVIC_DMA1_Channel1);
    f_base_nvic_flag_set(m_NVIC_DMA1_Channel2_3);
    f_base_nvic_flag_set(m_NVIC_DMAMUX1_DMA1_CH4_5);
    f_base_nvic_flag_set(m_NVIC_TIM1_CC);
    f_base_nvic_flag_set(m_NVIC_TIM3);
}
//===================================================================
/*### Interrupt Setting
-------------------------------------------------------------------*/
void f_base_nvic_flag_set(te_nvic e_nvic)
{
    NVIC->ISER[0] = (0x01 << e_nvic);
}
//===================================================================
/*### Interrupt setting clear
-------------------------------------------------------------------*/
void f_base_nvic_clear_set(te_nvic e_nvic)
{
    NVIC->ICER[0] = (0x01 << e_nvic);
}
//===================================================================
/*### Interrupt Call reset
-------------------------------------------------------------------*/
void f_base_nvic_pending_clear(te_nvic e_nvic)
{
    NVIC->ICPR[0] = (0x01 << e_nvic);
}
//===================================================================
/*### Priority Set
-------------------------------------------------------------------*/
void f_base_nvic_priority_set(te_irq e_irq, tu32 v_priority)
{
    tu32 v_ip_num = (tu32)(0x04 & (e_irq >> 16));
    tu32 v_shift = (tu32)(0x04 & e_irq);
    NVIC->IP[v_ip_num] |= ((v_priority & 0x04) << v_shift);
}
