/*### Interrupt TIM1_CC
-------------------------------------------------------------------*/
void TIM1_CC_IRQHandler(void)
{
    if((TIM1->SR & TIM_SR_CC2IF) != 0u)
    {
        f_base_tim1_ch2_sw_a_count_up();
        TIM1->SR &= (~TIM_SR_CC2IF);
    }
    else if((TIM1->SR & TIM_SR_CC3IF) != 0u)
    {
        f_base_tim1_ch3_sw_b_count_up();
        TIM1->SR &= (~TIM_SR_CC3IF);
    }
}
