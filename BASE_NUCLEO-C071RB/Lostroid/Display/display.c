/*### Print Digit Dec64  "자리수 고정 64bit 숫자 출력"
---------------------------------------------------------------------
DigtNum:5, 100 -> "00100"
+ v_min_digits : 32bit Fixed  "자리수 고정"
+ v_dec : 64bit             "64비트 입력"
-------------------------------------------------------------------*/
void f_display_conversion_dec64_digit(char *p_save, tu32 *p_pos, tu32 v_min_digits, tu64 v_dec)
{
    char a_data[20];
    tu8 v_index = 20u;
    tu32 v_total_len = 0u;

    if(p_save == d_NULL ||  p_pos == d_NULL)
        { return; }
    if(v_min_digits == 0) 
        { return; }

    if(v_min_digits > 20u)
        { v_min_digits = 20u; }

    //+ number convert 
    if(v_dec == 0)
    {
        a_data[--v_index] = '0';
        v_total_len++;
    }
    else
    {
        while(v_dec > 0 && v_index > 0)
        {
            a_data[--v_index] = (v_dec % 10u) + '0';
            v_dec /= 10u;
        }
        v_total_len = 20u - v_index;
    }
    //= zero padding (minimum width)
    while(v_total_len < v_min_digits)
    {
        a_data[--v_index] = '0';
        v_total_len++;
    }
    while (v_total_len > 0u)
    {
        p_save[*p_pos] = a_data[v_index++];
        (*p_pos)++;
        v_total_len--;
    }    
}
