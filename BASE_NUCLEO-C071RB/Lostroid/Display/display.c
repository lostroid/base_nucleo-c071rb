/*### Print Dec64 "64비트 숫자 출력"
---------------------------------------------------------------------
+ v_dec : 64bit "64bit 입력"
-------------------------------------------------------------------*/
void f_display_conversion_dec64(char *p_save, tu32 *p_pos, tu64 v_dec)
{
    char  v_buf[20];
    tu8   v_len = 0u;

    if(p_save == d_NULL ||  p_pos == d_NULL)
        { return; }

    if (v_dec == 0u)
    {
        p_save[*p_pos] = '0';
        (*p_pos)++;
        return;
    }

    while (v_dec > 0u)
    {
        v_buf[v_len++] = (char)((v_dec % 10u) + '0');
        v_dec /= 10u;
    }
    while (v_len > 0u)
    {
        p_save[*p_pos] = v_buf[--v_len];
        (*p_pos)++;
    }
}
